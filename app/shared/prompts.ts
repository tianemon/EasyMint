// ── 业务 Prompt 构建函数 ────────────────────────────

/** 项目创建时的需求收集 */
export function buildProjectCreatedPrompt(ctx: string): string {
  return `[系统通知] 用户点击了新建项目。请了解以下需求信息，无需回复：\n${ctx}\n收到后只需回复"已记录"。`;
}

/** 功能清单推荐 */
export function buildFeatureRecommendPrompt(ctx: string): string {
  return `请根据以下项目信息推荐功能清单：${ctx}

要求：
- 只输出功能名称，一行一个，格式为 "- 功能名称"
- 不要解释、不要分析、不要分类（不要写 P0/P1/P2）
- 不要输出"为什么选择"或其他任何文字
- 只输出纯功能列表，就像这样：
- 功能一
- 功能二`;
}

/** 项目创建完毕后的初始化触发 */
export function buildInitTriggerPrompt(projectPath: string, ctx: string, instruction: string): string {
  return `[系统通知] 项目已创建完毕。\n\n项目路径：${projectPath}\n\n${ctx}\n\n${instruction}`;
}
